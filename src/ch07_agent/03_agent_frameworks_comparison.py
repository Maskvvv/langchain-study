def demo_langgraph_concept():
    """演示 LangGraph 的核心概念（概念说明）"""
    # ========================================
    # LangGraph 核心概念
    # ========================================
    # LangGraph 是 LangChain 官方推荐的 Agent 构建框架
    # 它的核心思想是用"状态图"来定义 Agent 的工作流
    #
    # 核心概念：
    # 1. State（状态）：Agent 在执行过程中维护的数据
    # 2. Node（节点）：执行具体操作的函数
    # 3. Edge（边）：节点之间的转移条件
    # 4. Conditional Edge（条件边）：根据状态决定下一个节点
    #
    # 示例图结构：
    #
    #   ┌─────────┐
    #   │  START  │
    #   └────┬────┘
    #        │
    #        ▼
    #   ┌─────────┐     ┌──────────────┐
    #   │  Agent  │────→│  Should Continue?  │
    #   └────┬────┘     └──────┬───────┘
    #        │                 │
    #        │ Yes             │ No
    #        ▼                 ▼
    #   ┌─────────┐     ┌─────────┐
    #   │  Tools  │     │   END   │
    #   └────┬────┘     └─────────┘
    #        │
    #        └──────→ Agent (循环)

    print("""
╔══════════════════════════════════════════════════╗
║            LangGraph 核心概念                     ║
╠══════════════════════════════════════════════════╣
║                                                  ║
║  State（状态）                                   ║
║  ├── messages: 消息列表                          ║
║  ├── next: 下一个要执行的节点                    ║
║  └── ...: 自定义字段                             ║
║                                                  ║
║  Node（节点）                                    ║
║  ├── agent_node: 调用 LLM，决定下一步            ║
║  ├── tool_node: 执行工具                         ║
║  └── custom_node: 自定义逻辑                     ║
║                                                  ║
║  Edge（边）                                      ║
║  ├── START → agent_node                          ║
║  ├── agent_node → tool_node (需要工具时)         ║
║  ├── agent_node → END (不需要工具时)             ║
║  └── tool_node → agent_node (循环)               ║
║                                                  ║
║  优势：                                          ║
║  ✅ 精细控制每一步                               ║
║  ✅ 支持循环和条件分支                           ║
║  ✅ 支持人机协作（Human-in-the-Loop）            ║
║  ✅ 可视化工作流                                 ║
║  ✅ 支持持久化和断点续传                         ║
║                                                  ║
╚══════════════════════════════════════════════════╝
""")


def demo_crewai_concept():
    """演示 CrewAI 的核心概念（概念说明）"""
    # ========================================
    # CrewAI 核心概念
    # ========================================
    # CrewAI 是一个多 Agent 协作框架
    # 核心思想：让多个具有不同角色的 Agent 协作完成任务
    #
    # 核心概念：
    # 1. Agent：具有角色、目标和工具的智能体
    # 2. Task：需要完成的任务
    # 3. Crew：Agent 和 Task 的组织，定义协作方式
    #
    # 示例：
    # Agent 1: 研究员（负责搜索和收集信息）
    # Agent 2: 写手（负责撰写文章）
    # Agent 3: 审核员（负责审核和修改）
    #
    # Task 1: 研究主题 → Agent 1
    # Task 2: 撰写文章 → Agent 2（基于 Task 1 的结果）
    # Task 3: 审核修改 → Agent 3（基于 Task 2 的结果）

    print("""
╔══════════════════════════════════════════════════╗
║            CrewAI 核心概念                        ║
╠══════════════════════════════════════════════════╣
║                                                  ║
║  Agent（智能体）                                 ║
║  ├── role: 角色描述（如"资深研究员"）            ║
║  ├── goal: 目标（如"发现突破性技术"）            ║
║  ├── backstory: 背景故事                         ║
║  └── tools: 可用工具列表                         ║
║                                                  ║
║  Task（任务）                                    ║
║  ├── description: 任务描述                       ║
║  ├── agent: 负责的 Agent                         ║
║  └── context: 依赖的前置任务                     ║
║                                                  ║
║  Crew（团队）                                    ║
║  ├── agents: Agent 列表                          ║
║  ├── tasks: Task 列表                            ║
║  └── process: 协作方式（顺序/层级）              ║
║                                                  ║
║  协作模式：                                      ║
║  ┌──────┐    ┌──────┐    ┌──────┐               ║
║  │研究员 │ →  │ 写手 │ →  │审核员│               ║
║  └──────┘    └──────┘    └──────┘               ║
║  (Sequential Process)                            ║
║                                                  ║
║  优势：                                          ║
║  ✅ 多角色协作简单                               ║
║  ✅ 角色定义清晰                                 ║
║  ✅ 适合内容创作等协作场景                       ║
║                                                  ║
╚══════════════════════════════════════════════════╝
""")


def demo_autogen_concept():
    """演示 AutoGen 的核心概念（概念说明）"""
    # ========================================
    # AutoGen 核心概念
    # ========================================
    # AutoGen 是 Microsoft 开发的多 Agent 对话框架
    # 核心思想：Agent 之间通过对话来协作完成任务
    #
    # 核心概念：
    # 1. ConversableAgent：可对话的 Agent
    # 2. AssistantAgent：AI 助手 Agent
    # 3. UserProxyAgent：代表用户的 Agent
    # 4. GroupChat：多 Agent 群聊

    print("""
╔══════════════════════════════════════════════════╗
║            AutoGen 核心概念                       ║
╠══════════════════════════════════════════════════╣
║                                                  ║
║  ConversableAgent（可对话 Agent）                ║
║  ├── 可以发送和接收消息                          ║
║  ├── 可以执行代码                                ║
║  └── 可以调用 LLM                               ║
║                                                  ║
║  AssistantAgent（AI 助手）                       ║
║  ├── 默认由 LLM 驱动                            ║
║  └── 可以编写和执行代码                          ║
║                                                  ║
║  UserProxyAgent（用户代理）                      ║
║  ├── 代表人类用户                                ║
║  ├── 可以执行代码                                ║
║  └── 可以请求人类输入                            ║
║                                                  ║
║  对话模式：                                      ║
║  ┌────────┐          ┌──────────┐               ║
║  │ 用户   │ ←──────→ │ AI 助手  │               ║
║  └────────┘          └──────────┘               ║
║       ↕                   ↕                      ║
║  ┌──────────────────────────────┐               ║
║  │       代码执行器             │               ║
║  └──────────────────────────────┘               ║
║                                                  ║
║  优势：                                          ║
║  ✅ 人机协作自然                                 ║
║  ✅ 代码执行能力强                               ║
║  ✅ 灵活的对话模式                               ║
║  ✅ Microsoft 生态支持                           ║
║                                                  ║
╚══════════════════════════════════════════════════╝
""")


